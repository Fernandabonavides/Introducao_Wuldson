# Entrada dos números 
num1 = int(input("Digite o primeiro número inteiro: "))
num2 = int(input("Digite o segundo número inteiro: "))
num3 = float(input("Digite o número real: "))

# Dobro do primeiro com metade do segundo
produto = (2 * num1) * (num2 / 2)
print("O produto do dobro do primeiro com metade do segundo é {}:" .format(produto))

# Soma do triplo do primeiro com o terceiro
soma = (3 * num1) + num3
print("A soma do triplo do primeiro com o terceiro é {}:" .format(soma))

# Calcula e exibe o terceiro elevado ao cubo
cubo = num3 ** 3
print("O terceiro número elevado ao cubo é {}:" .format(cubo))

